function renderSprite(config, pixelSize=4, animated=false) {
  const {type='hero',skinColor='#F5CBA7',hairColor='#27AE60',outfitColor='#27AE60',accentColor='#F39C12'}=config;
  const colorMap={H:hairColor,S:skinColor,O:outfitColor,A:accentColor,K:'#1a1a2e',B:'#6b4226',G:'#f1c40f'};
  const data=SPRITE_DEFS[type]||SPRITE_DEFS.hero;
  const svgW=16*pixelSize,svgH=16*pixelSize;
  let rects='';
  for(const [layer,coords] of Object.entries(data)){const fill=colorMap[layer];if(!fill||!coords.length)continue;for(const [x,y] of coords)rects+='<rect x="'+(x*pixelSize)+'" y="'+(y*pixelSize)+'" width="'+pixelSize+'" height="'+pixelSize+'" fill="'+fill+'"/>';}
  const animStyle=animated?'<style>@keyframes bob{0%,100%{transform:translateY(0)}50%{transform:translateY(-'+pixelSize+'px)}} .sprite-bob{animation:bob 1.5s ease-in-out infinite}</style>':'';
  return '<svg xmlns="http://www.w3.org/2000/svg" width="'+svgW+'" height="'+svgH+'" viewBox="0 0 '+svgW+' '+svgH+'" style="image-rendering:pixelated;image-rendering:crisp-edges">'+animStyle+'<g class="'+(animated?'sprite-bob':'')+'">' +rects+'</g></svg>';
}
function getXpForLevel(level){return (level-1)*(level-1)*50;}
function getLevelFromXp(xp){return Math.floor(Math.sqrt(xp/50))+1;}
function getXpProgress(xp,level){const current=getXpForLevel(level),next=getXpForLevel(level+1);return{current,next,progress:Math.min(100,((xp-current)/(next-current))*100)};}
